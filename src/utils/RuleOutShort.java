package utils;

public class RuleOutShort extends Exception {

    public RuleOutShort() {
        super();
    }

    public RuleOutShort(String message) {
        super(message);
    }

    public RuleOutShort(String message, Throwable cause) {
        super(message, cause);
    }

    public RuleOutShort(Throwable cause) {
        super(cause);
    }

}
